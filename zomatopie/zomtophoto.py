class ZomatoPhoto():
    def __init__(self, photo_id, url, thumb_url, user, res_id, caption, timestamp, friendly_time, width, height, comments_count, likes_count):
        self.photo_id = photo_id
        self.url = url
        self.thumb_url = thumb_url
        self.user = user
        self.res_id = res_id
        self.caption = caption
        self.timestamp = timestamp
        self.friendly_time = friendly_time
        self.width = width
        self.height = height
        self.comments_count = comments_count
        self.likes_count = likes_count
